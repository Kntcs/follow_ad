import re

def md_to_html(text):
    """将 Markdown 转换为 HTML"""
    # 转换表格
    table_pattern = r'\|(.+)\|\n\|[-:\s|]+\|\n((?:\|.+\|\n?)+)'

    def convert_table(match):
        header = match.group(1)
        rows = match.group(2)

        # 处理表头
        headers = [h.strip() for h in header.split('|') if h.strip()]
        html = '<table style="width:100%; border-collapse: collapse; margin: 12px 0;">'
        html += '<thead><tr>'
        for h in headers:
            html += f'<th style="border: 1px solid #ddd; padding: 8px; background: #f0f7ff; text-align: left;">{h}</th>'
        html += '</tr></thead><tbody>'

        # 处理数据行
        for row in rows.strip().split('\n'):
            if row.strip():
                cells = [c.strip() for c in row.split('|') if c.strip()]
                html += '<tr>'
                for cell in cells:
                    html += f'<td style="border: 1px solid #ddd; padding: 8px;">{cell}</td>'
                html += '</tr>'

        html += '</tbody></table>'
        return html

    text = re.sub(table_pattern, convert_table, text, flags=re.MULTILINE)

    # 转换标题
    text = re.sub(r'^### (.+)$', r'<h3>\1</h3>', text, flags=re.MULTILINE)
    text = re.sub(r'^\*\*(\d+)\.\s*(.+)\*\*$', r'<h4>\1. \2</h4>', text, flags=re.MULTILINE)

    # 转换粗体
    text = re.sub(r'\*\*([^*]+)\*\*', r'<strong>\1</strong>', text)

    # 转换斜体
    text = re.sub(r'\*([^*]+)\*', r'<em>\1</em>', text)

    # 转换列表项（- 开头）
    text = re.sub(r'^- (.+)$', r'<li>\1</li>', text, flags=re.MULTILINE)

    # 转换段落（连续的非空行）
    lines = text.split('\n')
    result = []
    in_list = False

    for i, line in enumerate(lines):
        line = line.strip()

        if line.startswith('<li>'):
            if not in_list:
                result.append('<ul>')
                in_list = True
            result.append(line)
        else:
            if in_list:
                result.append('</ul>')
                in_list = False

            if line.startswith('<h'):
                result.append(line)
            elif line:
                result.append(f'<p>{line}</p>')
            elif result and not result[-1].startswith('<'):
                result.append('<br>')

    if in_list:
        result.append('</ul>')

    return '\n'.join(result)

with open("${REPORT_PATH}", 'r') as f:
